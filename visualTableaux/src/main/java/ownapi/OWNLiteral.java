package ownapi;

public class OWNLiteral extends OWNAxiom {
	private String id;
	private String fullName;
	
	public OWNLiteral(String id, String fullName) {
		super(null, null);
		super.type = AXIOM_TYPE.LITERAL;
	}
	
	public String getId() {
		return id;
	}
	
	public String getFullName() {
		return fullName;
	}

}

package ver1;

import ownapi.OWNAxiom;

public class NonDeterministicOperation {
	private Node n;
	private Operation op;
	private OWNAxiom result;
	
	public NonDeterministicOperation(Node n, Operation op, OWNAxiom result) {
		this.n = n;
		this.op = op;
		this.result = result;
	}

	public Node getN() {
		return n;
	}

	public Operation getOp() {
		return op;
	}

	public OWNAxiom getResult() {
		return result;
	}
	
}
